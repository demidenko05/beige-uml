package org.beigesoft.graphic.pojo;

public class SettingsFont {

  private boolean isBold;
  
  private boolean isUnderlining;
  
  private float size;

  public boolean getIsBold() {
    return isBold;
  }

  public void setIsBold(boolean isBold) {
    this.isBold = isBold;
  }

  public boolean getIsUnderlining() {
    return isUnderlining;
  }

  public void setIsUnderlining(boolean isUnderlining) {
    this.isUnderlining = isUnderlining;
  }

  public float getSize() {
    return size;
  }

  public void setSize(float size) {
    this.size = size;
  }
}
